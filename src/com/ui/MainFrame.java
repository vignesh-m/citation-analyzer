    private void updateText(int newmode){
        //mode  1 - basic , 2 - verbose 
        displaymode=newmode;
        mainTextArea.setText("");
        for(Entry e:currentlist){
            if(displaymode==1) mainTextArea.append(e.toString_basic()+"\n");
            if(displaymode==2) mainTextArea.append(e.toString()+"\n");
        }
    }
    private void updateText(){
        //mode  1 - basic , 2 - verbose 
        mainTextArea.setText("");
        for(Entry e:currentlist){
            if(displaymode==1) mainTextArea.append(e.toString_basic()+"\n");
            if(displaymode==2) mainTextArea.append(e.toString()+"\n");
        }
    }
