    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String authorname=authorNameInput.getText();
        String numresultstr=resultnumInput.getText();
        mainTextArea.setText("Fetching results . . . ");
        errorLabel.setText("");
        int numresult;
        try{
            numresult=Integer.parseInt(numresultstr.trim());
            currentlist=parser.parsePage(1, authorname, numresult);
            updateText(1);
        } catch(NumberFormatException e){
            errorLabel.setText("Enter a proper number !");
        } catch(Exception e){
            ;
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void tostringtoggleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tostringtoggleActionPerformed
        if(tostringtoggle.isSelected()) {
            updateText(2);
            //tostringtoggle.setText("Less info");
        } else {
            updateText(1);
            tostringtoggle.setText("More info");
        }
    }//GEN-LAST:event_tostringtoggleActionPerformed

    private void sortByDateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortByDateBtnActionPerformed
        boolean asc=!sortDateStatus.getName().equals("dec");
        if(asc){
            sortDateStatus.setName("dec");
            sortDateStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/ui/down_arrow.png"))); 
        } else {
            sortDateStatus.setName("inc");
            sortDateStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/ui/up_arrow.png"))); 
        }
        System.out.println(asc);
        currentlist.sortByYear(asc);
        updateText();
    }//GEN-LAST:event_sortByDateBtnActionPerformed

